def _has_rdkit_and_pandas(m):
    try:
        m._load_heavy()
        return bool(getattr(m, "_CHEM_READY", False)) and getattr(m, "pd", None) is not None
    except Exception:
        return False


def _write_one_row_csv(tmp_path, filename, smiles):
    p = tmp_path / filename
    p.write_text(f"NAME,SMILES\nX,{smiles}\n", encoding="utf-8")
    return str(p)


