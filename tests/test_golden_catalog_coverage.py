def test_golden_catalog_coverage(tmp_path):
    import mcrg_desktop as m

    if not _has_rdkit_and_pandas(m):
        return

    for mcr_key, info in m.MCR_CATALOGO.items():
        case = CASES.get(mcr_key)

        if case is None:
            # For reactions without a stable RDKit-parseable reactant setup in this version,
            # assert the engine returns an empty dataframe with a stable preview warning.
            files = []
            for i, _comp in enumerate(info["componentes"]):
                files.append(_write_one_row_csv(tmp_path, f"c{i}.csv", "NOT_A_SMILES"))
            df_all, df_ideal, preview = m.run_mcr(mcr_key, files, core_smiles_list=None, threshold=0.0, ideal_rule="Lipinski")
            assert len(df_all) == 0
            assert "empty or invalid" in preview
            continue

        files = []
        for i, smi in enumerate(case["component_smiles"]):
            files.append(_write_one_row_csv(tmp_path, f"{mcr_key}_{i}.csv", smi))
        core = case.get("core")
        core_list = [tuple(core)] if core else None

        df_all, df_ideal, preview = m.run_mcr(mcr_key, files, core_smiles_list=core_list, threshold=0.0, ideal_rule="Lipinski")
        assert len(df_all) == 1, f"{mcr_key} expected 1 row, got {len(df_all)}. preview={preview}"

        exp = case["expect"]
        if exp["type"] == "smiles":
            assert df_all.iloc[0]["SMILES_Final"] == exp["value"]
        elif exp["type"] == "failure_reason":
            assert df_all.iloc[0]["Failure_Reason"] == exp["value"]
        else:
            raise AssertionError(f"Unknown expect type for {mcr_key}: {exp['type']}")
