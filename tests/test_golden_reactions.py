def test_golden_mannich_no_product_reason(tmp_path):
    import mcrg_desktop as m

    if not _has_rdkit_and_pandas(m):
        return
    if "Mannich (3-CR)" not in m.MCR_CATALOGO:
        return

    alde = _write_one_row_csv(tmp_path, "alde.csv", "O=Cc1ccccc1")
    amine = _write_one_row_csv(tmp_path, "amine.csv", "CN")
    keto = _write_one_row_csv(tmp_path, "keto.csv", "CC(=O)CC(=O)OC")

    df_all, df_ideal, _ = m.run_mcr("Mannich (3-CR)", [alde, amine, keto], threshold=0.0, ideal_rule="Lipinski")
    assert len(df_all) == 1
    assert len(df_ideal) == 0
    assert df_all.iloc[0]["Classification"] == "Discarded"
    assert df_all.iloc[0]["Review_Status"] == "Error"
    assert df_all.iloc[0]["Failure_Reason"] == "No products generated by reaction SMARTS"


